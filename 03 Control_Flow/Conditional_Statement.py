temperature = 15
if temperature > 30:  # Whenever you use an if statement you should always terminate it with a ":"
    # Always indent when inside an if block otherwise your code will not
    print("It's Hot")
    print("Drink Water")
elif temperature > 20:  # Used for when you want to have multiple conditions.
    print("It's nice")
else:
    print("It's cold")
print("Done")  # Remove indentation to indictae the end of this if block
