# The difference between print() and greet() is:
# print() takes an input
# greet() doesnt take an input
def greet(first_name, last_name):  # A  parameter is an input that you define for your arguments
    print(f"Hi there {first_name} {last_name}")
    print("Welcome aboard")

# An argument is the actual value for a given parameter
greet("Sibahle","Dube")
